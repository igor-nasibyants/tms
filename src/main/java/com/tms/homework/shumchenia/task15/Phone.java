package com.tms.homework.shumchenia.task15;

public class Phone {
    public String name;
    public int coast;

    public Phone(String name, int coast) {
        this.name = name;
        this.coast = coast;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCoast() {
        return coast;
    }

    public void setCoast(int coast) {
        this.coast = coast;
    }
}
